package fr.umlv.irsensor.common.data;

public class MimetypeException extends Exception {

  private static final long serialVersionUID = -7460666578021673472L;

  public MimetypeException() {
    super();
  }

  public MimetypeException(String arg0, Throwable arg1) {
    super(arg0, arg1);
  }

  public MimetypeException(String arg0) {
    super(arg0);
  }

  public MimetypeException(Throwable arg0) {
    super(arg0);
  }

}
